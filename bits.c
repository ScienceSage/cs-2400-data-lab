/* 
 * fitsBits - return 1 if x can be represented as an 
 *  n-bit, two's complement integer.
 *   1 <= n <= 32
 *   Examples: fitsBits(5,3) = 0, fitsBits(-4,3) = 1
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 15
 *   Rating: 2
 */
int fitsBits(int x, int n) {
  /* get the sign value, then get the largest value that ignores the sign, then shift and see if there are any bits that can't fit */
  int sign = x >> 31;
  int largest = (~x & sign) + (x & ~sign);
  int n_min_1 = n + ~0;
  return !(largest >> n_min_1);
}
/* 
 * divpwr2 - Compute x/(2^n), for 0 <= n <= 30
 *  Round toward zero
 *   Examples: divpwr2(15,1) = 7, divpwr2(-33,4) = -2
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 15
 *   Rating: 2
 */
int divpwr2(int x, int n) {
  /* just do x >> n if positive, x >> n + 1 if negative */
  // 2^n - 1
  int denom = (1 << n) + ~0;
  int change = (x >> 31) & denom;
  return (x + change) >> n;
}
/* 
 * isNotEqual - return 0 if x == y, and 1 otherwise 
 *   Examples: isNotEqual(5,5) = 0, isNotEqual(4,5) = 1
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 6
 *   Rating: 2
 */
int isNotEqual(int x, int y) {
  /* xor finds any differences, then uses ! to output */
  int dif = x ^ y;
  return !!dif;
}
/* 
 * bitXor - x^y using only ~ and & 
 *   Example: bitXor(4, 5) = 1
 *   Legal ops: ~ &
 *   Max ops: 14
 *   Rating: 2
 */
int bitXor(int x, int y) {
  /* DeMorgan's law (~x & y) | (x & ~y) */
  return ~(~(~x & y) & ~(x & ~y));
}
/* 
 * copyLSB - set all bits of result to least significant bit of x
 *   Example: copyLSB(5) = 0xFFFFFFFF, copyLSB(6) = 0x00000000
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 5
 *   Rating: 2
 */
int copyLSB(int x) {
  /* Move least significant to the most, then bit shift right to copy the most signifigant */
  int most_sig = x << 31;
  return most_sig >> 31;
}
