/* 
 * abs - absolute value of x (except returns TMin for TMin)
 *   Example: abs(-1) = 1.
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 10
 *   Rating: 4
 */
int abs(int x) {
  /* get sign and xor with that (only affects negatives), then add one if negative */
  int sign = x >> 31;
  int xor = x ^ sign;
  return xor + (~sign + 1);
}
/*
 * bitCount - returns count of number of 1's in word
 *   Examples: bitCount(5) = 2, bitCount(7) = 3
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 40
 *   Rating: 4
 */
int bitCount(int x) {
  /* counts each byte, then adds each count from each byte */
  int mask = 1 + (1 << 8) + (1 << 16) + (1 << 24);
  int count = x & mask;
  int sum = 0;
  count += (x >> 1) & mask;
  count += (x >> 2) & mask;
  count += (x >> 3) & mask;
  count += (x >> 4) & mask;
  count += (x >> 5) & mask;
  count += (x >> 6) & mask;
  count += (x >> 7) & mask;
  
  mask = 255;
  sum = count & mask;
  sum += (count >> 8) & mask;
  sum += (count >> 16) & mask;
  sum += (count >> 24) & mask;
  return sum;
}
/* 
 * isNonZero - Check whether x is nonzero using
 *              the legal operators except !
 *   Examples: isNonZero(3) = 1, isNonZero(0) = 0
 *   Legal ops: ~ & ^ | + << >>
 *   Max ops: 10
 *   Rating: 4 
 */
int isNonZero(int x) {
  /* folds the number until the least signifigant bit is 1 if there is a 1 anywhere in the bits
   * This uses 11 operators, though :( 
   */
  int half = x | (x >> 16);
  int quarter = half | (half >> 8);
  int eighth = quarter | (quarter >> 4);
  int sixt = eighth | (eighth >> 2);
  return (sixt | (sixt >> 1)) & 1;
}
/* 
 * bang - Compute !x without using !
 *   Examples: bang(3) = 0, bang(0) = 1
 *   Legal ops: ~ & ^ | + << >>
 *   Max ops: 12
 *   Rating: 4 
 */
int bang(int x) {
  /* same as previous code, but invert the least signifigant bit */
  int half = x | (x >> 16);
  int quarter = half | (half >> 8);
  int eighth = quarter | (quarter >> 4);
  int sixt = eighth | (eighth >> 2);
  return ((sixt | (sixt >> 1)) + 1) & 1;
}
